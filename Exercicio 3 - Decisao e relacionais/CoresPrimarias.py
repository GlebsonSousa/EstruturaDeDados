cor = input(str(" Digite uma cor primária:"))

print("\n")

if cor != "Azul" and cor != "Amarelo" and cor != "Vermelho":

  print(" Essa cor não é primária!!!")

elif cor == "Azul":
  
  print(" Azul é uma cor calma, que representa inteligência e representa também o frio.")
  
elif cor == "Amarelo":

  print(" Amarelo é uma cor alegre, que representa riqueza e luz.")
  
elif cor == "Vermelho":

  print(" Vermelho é uma cor calorosa, que representa energia e perigo.")